import 'doctor_summary_provider.dart';

class DoctorSummaryResultScreen extends StatelessWidget {
  const DoctorSummaryResultScreen({super.key});

  String _buildExportText(dynamic summary) {
    final buffer = StringBuffer();
    buffer.writeln('DOCTOR VISIT SUMMARY');
    buffer.writeln(
      'Generated: ${DateFormat('dd MMM yyyy, hh:mm a').format(summary.generatedAt)}',
    );
    buffer.writeln();
    buffer.writeln('─' * 40);
    buffer.writeln();
    buffer.writeln('PROFILE');
    buffer.writeln(summary.profileSnapshot);

    if (summary.recentSymptoms.isNotEmpty) {
      buffer.writeln();
      buffer.writeln('RECENT SYMPTOMS');
      for (final s in summary.recentSymptoms) {
        buffer.writeln('• $s');
      }
    }

    if (summary.periodHistory.isNotEmpty) {
      buffer.writeln();
      buffer.writeln('PERIOD HISTORY');
      for (final p in summary.periodHistory) {
        buffer.writeln('• $p');
      }
    }

    if (summary.uploadedRecordTitles.isNotEmpty) {
      buffer.writeln();
      buffer.writeln('HEALTH RECORDS');
      for (final r in summary.uploadedRecordTitles) {
        buffer.writeln('• $r');
      }
    }

    if (summary.currentMedications.isNotEmpty) {
      buffer.writeln();
      buffer.writeln('CURRENT MEDICATIONS');
      for (final m in summary.currentMedications) {
        buffer.writeln('• $m');
      }
    }

    buffer.writeln();
    buffer.writeln('QUESTIONS FOR DOCTOR');
    for (final q in summary.questionsForDoctor) {
      buffer.writeln('• $q');
    }

    if (summary.userNotes != null) {
      buffer.writeln();
      buffer.writeln('ADDITIONAL NOTES');
      buffer.writeln(summary.userNotes);
    }

    buffer.writeln();
    buffer.writeln('─' * 40);
    buffer.writeln('Generated by Sparkle Lite — not a medical document');
    return buffer.toString();
  }

  Future<void> _saveTextFile(BuildContext context, dynamic summary) async {
    final text = _buildExportText(summary);
    final fileName =
        'sparkle_doctor_summary_${summary.generatedAt.millisecondsSinceEpoch}.txt';

    try {
      final directory = kIsWeb
          ? null
          : await getApplicationDocumentsDirectory(); // or a Downloads-specific dir on Android
      if (kIsWeb) {
        // web: trigger a browser download instead of Share
        // e.g. using package:web / dart:html Blob + anchor click
        return;
      }
      final file = File('${directory!.path}/$fileName');
      await file.writeAsString(text);

      if (!context.mounted) return;
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Text('Saved to ${file.path}'),
          backgroundColor: AppTheme.success,
        ),
      );
    } catch (e) {
      if (!context.mounted) return;
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(
          content: Text('Failed to save file'),
          backgroundColor: AppTheme.error,
        ),
      );
    }
  }

  Future<void> _shareTextFile(BuildContext context, dynamic summary) async {
    final text = _buildExportText(summary);
    final fileName =
        'sparkle_doctor_summary_${summary.generatedAt.millisecondsSinceEpoch}.txt';
    final directory = await getApplicationDocumentsDirectory();
    final file = File('${directory.path}/$fileName');
    await file.writeAsString(text);

    if (!context.mounted) return;
    await SharePlus.instance.share(
      ShareParams(files: [XFile(file.path)], subject: 'Doctor Visit Summary'),
    );
  }

  @override
  Widget build(BuildContext context) {
    final provider = context.watch<DoctorSummaryProvider>();
    final summary = provider.currentSummary;

    if (summary == null) {
      return Scaffold(
        backgroundColor: context.bg,
        body: Center(
          child: Text(
            'No summary available',
            style: TextStyle(color: context.textSecondary),
          ),
        ),
      );
    }

    return Scaffold(
      backgroundColor: context.bg,
      appBar: AppBar(
        title: const Text('Visit Summary'),
        automaticallyImplyLeading: false,
        actions: [
          IconButton(
            icon: const Icon(Icons.copy_outlined),
            tooltip: 'Copy summary',
            onPressed: () async {
              final privacy = context.read<PrivacyProvider>();

              if (privacy.requireConfirmation) {
                final confirm = await showDialog<bool>(
                  context: context,
                  builder: (ctx) => AlertDialog(
                    title: const Text('Copy Summary'),
                    content: const Text(
                      'This summary contains your personal health '
                      'information. Are you sure you want to copy it '
                      'to clipboard?',
                    ),
                    actions: [
                      TextButton(
                        onPressed: () => Navigator.pop(ctx, false),
                        child: const Text('Cancel'),
                      ),
                      TextButton(
                        onPressed: () => Navigator.pop(ctx, true),
                        child: const Text('Copy'),
                      ),
                    ],
                  ),
                );
                if (confirm != true) return;
              }

              await Clipboard.setData(
                ClipboardData(text: _buildExportText(summary)),
              );
              if (!context.mounted) return;
              ScaffoldMessenger.of(context).showSnackBar(
                const SnackBar(
                  content: Text('Summary copied to clipboard ✓'),
                  backgroundColor: AppTheme.success,
                ),
              );
            },
          ),
        ],
      ),
      body: SingleChildScrollView(
        padding: const EdgeInsets.all(24),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Row(
              children: [
                Icon(
                  Icons.access_time_outlined,
                  size: 14,
                  color: context.textSecondary,
                ),
                const SizedBox(width: 6),
                Text(
                  'Generated ${summary.generatedAt.timeAgo}',
                  style: TextStyle(color: context.textSecondary, fontSize: 12),
                ),
              ],
            ),
            const SizedBox(height: 20),

            _SummarySection(
              title: '👤 Profile',
              content: summary.profileSnapshot,
            ),
            const SizedBox(height: 16),

            if (summary.recentSymptoms.isNotEmpty) ...[
              _SummaryListSection(
                title: '❤️ Recent Symptoms',
                items: summary.recentSymptoms,
              ),
              const SizedBox(height: 16),
            ],

            if (summary.periodHistory.isNotEmpty) ...[
              _SummaryListSection(
                title: '📅 Period History',
                items: summary.periodHistory,
              ),
              const SizedBox(height: 16),
            ],

            if (summary.uploadedRecordTitles.isNotEmpty) ...[
              _SummaryListSection(
                title: '🗂️ Health Records',
                items: summary.uploadedRecordTitles,
              ),
              const SizedBox(height: 16),
            ],

            if (summary.currentMedications.isNotEmpty) ...[
              _SummaryListSection(
                title: '💊 Current Medications',
                items: summary.currentMedications,
              ),
              const SizedBox(height: 16),
            ],

            _SummaryListSection(
              title: '🩺 Questions for Your Doctor',
              items: summary.questionsForDoctor,
              highlight: true,
            ),
            const SizedBox(height: 16),

            if (summary.userNotes != null) ...[
              _SummarySection(
                title: '📝 Your Notes',
                content: summary.userNotes!,
              ),
              const SizedBox(height: 16),
            ],

            Container(
              padding: const EdgeInsets.all(12),
              decoration: BoxDecoration(
                color: context.surfaceMuted,
                borderRadius: BorderRadius.circular(8),
              ),
              child: Text(
                'This summary is for personal use only and is not '
                'a medical document. Always consult a qualified '
                'healthcare professional.',
                style: TextStyle(
                  color: context.textSecondary,
                  fontSize: 11,
                  fontStyle: FontStyle.italic,
                ),
              ),
            ),
            const SizedBox(height: 32),

            OutlinedButton.icon(
              onPressed: () async {
                final privacy = context.read<PrivacyProvider>();
                if (privacy.requireConfirmation) {
                  final confirm = await showDialog<bool>(
                    context: context,
                    builder: (ctx) => AlertDialog(
                      title: const Text('Download Summary'),
                      content: const Text(
                        'This file contains your personal health '
                        'information. Proceed with download?',
                      ),
                      actions: [
                        TextButton(
                          onPressed: () => Navigator.pop(ctx, false),
                          child: const Text('Cancel'),
                        ),
                        TextButton(
                          onPressed: () => Navigator.pop(ctx, true),
                          child: const Text('Download'),
                        ),
                      ],
                    ),
                  );
                  if (confirm != true || !context.mounted) return;
                }
                await _saveTextFile(context, summary);
              },
              icon: const Icon(Icons.download_outlined),
              label: const Text('Download'),
            ),
            const SizedBox(height: 12),
            OutlinedButton.icon(
              onPressed: () async {
                final privacy = context.read<PrivacyProvider>();
                if (privacy.requireConfirmation) {
                  final confirm = await showDialog<bool>(
                    context: context,
                    builder: (ctx) => AlertDialog(
                      title: const Text('Share Summary'),
                      content: const Text(
                        'This file contains your personal health '
                        'information. Proceed with sharing?',
                      ),
                      actions: [
                        TextButton(
                          onPressed: () => Navigator.pop(ctx, false),
                          child: const Text('Cancel'),
                        ),
                        TextButton(
                          onPressed: () => Navigator.pop(ctx, true),
                          child: const Text('Share'),
                        ),
                      ],
                    ),
                  );
                  if (confirm != true || !context.mounted) return;
                }
                await _shareTextFile(context, summary);
              },
              icon: const Icon(Icons.share_outlined),
              label: const Text('Share'),
            ),
            const SizedBox(height: 12),
            ElevatedButton.icon(
              onPressed: provider.status == DoctorSummaryStatus.saved
                  ? null
                  : () async {
                      final success = await provider.saveSummaryToTimeline();
                      if (context.mounted) {
                        ScaffoldMessenger.of(context).showSnackBar(
                          SnackBar(
                            content: Text(
                              success
                                  ? 'Summary saved to timeline ✓'
                                  : 'Failed to save',
                            ),
                            backgroundColor: success
                                ? AppTheme.success
                                : AppTheme.error,
                          ),
                        );
                      }
                    },
              icon: Icon(
                provider.status == DoctorSummaryStatus.saved
                    ? Icons.check
                    : Icons.save_outlined,
              ),
              label: Text(
                provider.status == DoctorSummaryStatus.saved
                    ? 'Saved to Timeline'
                    : 'Save to Timeline',
              ),
            ),
            const SizedBox(height: 12),

            OutlinedButton(
              onPressed: () {
                provider.reset();
                Navigator.pop(context);
                Navigator.pop(context);
              },
              style: OutlinedButton.styleFrom(
                minimumSize: const Size(double.infinity, 52),
                side: const BorderSide(color: AppTheme.primary),
                shape: RoundedRectangleBorder(
                  borderRadius: BorderRadius.circular(12),
                ),
              ),
              child: const Text('Done'),
            ),
            const SizedBox(height: 24),
          ],
        ),
      ),
    );
  }
}

class _SummarySection extends StatelessWidget {
  const _SummarySection({required this.title, required this.content});

  final String title;
  final String content;

  @override
  Widget build(BuildContext context) {
    return Container(
      padding: const EdgeInsets.all(16),
      decoration: BoxDecoration(
        color: context.card,
        borderRadius: BorderRadius.circular(12),
        border: Border.all(color: context.border),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            title,
            style: TextStyle(
              fontWeight: FontWeight.bold,
              color: context.textPrimary,
            ),
          ),
          const SizedBox(height: 8),
          Text(
            content,
            style: TextStyle(color: context.textSecondary, height: 1.4),
          ),
        ],
      ),
    );
  }
}

class _SummaryListSection extends StatelessWidget {
  const _SummaryListSection({
    required this.title,
    required this.items,
    this.highlight = false,
  });

  final String title;
  final List<String> items;
  final bool highlight;

  @override
  Widget build(BuildContext context) {
    final highlightBg = context.isDarkMode
        ? AppTheme.primary.withValues(alpha: 0.1)
        : AppTheme.primary.withValues(alpha: 0.03);
    final highlightBorder = context.isDarkMode
        ? AppTheme.primary.withValues(alpha: 0.35)
        : AppTheme.primary.withValues(alpha: 0.2);

    return Container(
      padding: const EdgeInsets.all(16),
      decoration: BoxDecoration(
        color: highlight ? highlightBg : context.card,
        borderRadius: BorderRadius.circular(12),
        border: Border.all(color: highlight ? highlightBorder : context.border),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            title,
            style: TextStyle(
              fontWeight: FontWeight.bold,
              color: context.textPrimary,
            ),
          ),
          const SizedBox(height: 10),
          ...items.map(
            (item) => Padding(
              padding: const EdgeInsets.only(bottom: 8),
              child: Row(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(
                    '•  ',
                    style: TextStyle(
                      color: highlight
                          ? AppTheme.primary
                          : context.textSecondary,
                    ),
                  ),
                  Expanded(
                    child: Text(
                      item,
                      style: TextStyle(
                        color: highlight
                            ? context.textPrimary
                            : context.textSecondary,
                        height: 1.4,
                      ),
                    ),
                  ),
                ],
              ),
            ),
          ),
        ],
      ),
    );
  }
}
