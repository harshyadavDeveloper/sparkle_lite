import 'package:flutter/material.dart';
import 'package:flutter/services.dart';
import 'package:provider/provider.dart';
import 'package:smart_date_formatter/smart_date_formatter.dart';
import '../../core/theme/app_theme.dart';
import 'doctor_summary_provider.dart';

class DoctorSummaryResultScreen extends StatelessWidget {
  const DoctorSummaryResultScreen({super.key});

  String _buildExportText(dynamic summary) {
    final buffer = StringBuffer();
    buffer.writeln('DOCTOR VISIT SUMMARY');
    buffer.writeln(
      'Generated: ${summary.generatedAt.format('dd MMM yyyy, hh:mm a')}',
    );
    buffer.writeln('─' * 40);
    buffer.writeln('\nPROFILE');
    buffer.writeln(summary.profileSnapshot);
    if (summary.recentSymptoms.isNotEmpty) {
      buffer.writeln('\nRECENT SYMPTOMS');
      for (final s in summary.recentSymptoms) {
        buffer.writeln('• $s');
      }
    }
    if (summary.periodHistory.isNotEmpty) {
      buffer.writeln('\nPERIOD HISTORY');
      for (final p in summary.periodHistory) {
        buffer.writeln('• $p');
      }
    }
    if (summary.uploadedRecordTitles.isNotEmpty) {
      buffer.writeln('\nHEALTH RECORDS');
      for (final r in summary.uploadedRecordTitles) {
        buffer.writeln('• $r');
      }
    }
    if (summary.currentMedications.isNotEmpty) {
      buffer.writeln('\nCURRENT MEDICATIONS');
      for (final m in summary.currentMedications) {
        buffer.writeln('• $m');
      }
    }
    buffer.writeln('\nQUESTIONS FOR DOCTOR');
    for (final q in summary.questionsForDoctor) {
      buffer.writeln('• $q');
    }
    if (summary.userNotes != null) {
      buffer.writeln('\nADDITIONAL NOTES');
      buffer.writeln(summary.userNotes);
    }
    buffer.writeln('\n─' * 40);
    buffer.writeln('Generated by Sparkle Lite — not a medical document');
    return buffer.toString();
  }

  @override
  Widget build(BuildContext context) {
    final provider = context.watch<DoctorSummaryProvider>();
    final summary = provider.currentSummary;

    if (summary == null) {
      return const Scaffold(body: Center(child: Text('No summary available')));
    }

    return Scaffold(
      appBar: AppBar(
        title: const Text('Visit Summary'),
        automaticallyImplyLeading: false,
        actions: [
          // Copy button
          IconButton(
            icon: const Icon(Icons.copy_outlined),
            tooltip: 'Copy summary',
            onPressed: () {
              Clipboard.setData(ClipboardData(text: _buildExportText(summary)));
              ScaffoldMessenger.of(context).showSnackBar(
                const SnackBar(
                  content: Text('Summary copied to clipboard ✓'),
                  backgroundColor: AppTheme.success,
                ),
              );
            },
          ),
        ],
      ),
      body: SingleChildScrollView(
        padding: const EdgeInsets.all(24),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            // Generated date
            Row(
              children: [
                const Icon(
                  Icons.access_time_outlined,
                  size: 14,
                  color: AppTheme.textSecondary,
                ),
                const SizedBox(width: 6),
                Text(
                  'Generated ${summary.generatedAt.timeAgo}',
                  style: const TextStyle(
                    color: AppTheme.textSecondary,
                    fontSize: 12,
                  ),
                ),
              ],
            ),
            const SizedBox(height: 20),

            // Profile
            _SummarySection(
              title: '👤 Profile',
              content: summary.profileSnapshot,
            ),
            const SizedBox(height: 16),

            // Recent symptoms
            if (summary.recentSymptoms.isNotEmpty) ...[
              _SummaryListSection(
                title: '❤️ Recent Symptoms',
                items: summary.recentSymptoms,
              ),
              const SizedBox(height: 16),
            ],

            // Period history
            if (summary.periodHistory.isNotEmpty) ...[
              _SummaryListSection(
                title: '📅 Period History',
                items: summary.periodHistory,
              ),
              const SizedBox(height: 16),
            ],

            // Records
            if (summary.uploadedRecordTitles.isNotEmpty) ...[
              _SummaryListSection(
                title: '🗂️ Health Records',
                items: summary.uploadedRecordTitles,
              ),
              const SizedBox(height: 16),
            ],

            // Medications
            if (summary.currentMedications.isNotEmpty) ...[
              _SummaryListSection(
                title: '💊 Current Medications',
                items: summary.currentMedications,
              ),
              const SizedBox(height: 16),
            ],

            // Doctor questions
            _SummaryListSection(
              title: '🩺 Questions for Your Doctor',
              items: summary.questionsForDoctor,
              highlight: true,
            ),
            const SizedBox(height: 16),

            // User notes
            if (summary.userNotes != null) ...[
              _SummarySection(
                title: '📝 Your Notes',
                content: summary.userNotes!,
              ),
              const SizedBox(height: 16),
            ],

            // Disclaimer
            Container(
              padding: const EdgeInsets.all(12),
              decoration: BoxDecoration(
                color: Colors.grey.withValues(alpha: 0.08),
                borderRadius: BorderRadius.circular(8),
              ),
              child: const Text(
                'This summary is for personal use only and is not '
                'a medical document. Always consult a qualified '
                'healthcare professional.',
                style: TextStyle(
                  color: AppTheme.textSecondary,
                  fontSize: 11,
                  fontStyle: FontStyle.italic,
                ),
              ),
            ),
            const SizedBox(height: 32),

            // Save to timeline
            ElevatedButton.icon(
              onPressed: provider.status == DoctorSummaryStatus.saved
                  ? null
                  : () async {
                      final success = await provider.saveSummaryToTimeline();
                      if (context.mounted) {
                        ScaffoldMessenger.of(context).showSnackBar(
                          SnackBar(
                            content: Text(
                              success
                                  ? 'Summary saved to timeline ✓'
                                  : 'Failed to save',
                            ),
                            backgroundColor: success
                                ? AppTheme.success
                                : AppTheme.error,
                          ),
                        );
                      }
                    },
              icon: Icon(
                provider.status == DoctorSummaryStatus.saved
                    ? Icons.check
                    : Icons.save_outlined,
              ),
              label: Text(
                provider.status == DoctorSummaryStatus.saved
                    ? 'Saved to Timeline'
                    : 'Save to Timeline',
              ),
            ),
            const SizedBox(height: 12),

            OutlinedButton(
              onPressed: () {
                provider.reset();
                Navigator.pop(context);
                Navigator.pop(context);
              },
              style: OutlinedButton.styleFrom(
                minimumSize: const Size(double.infinity, 52),
                side: const BorderSide(color: AppTheme.primary),
                shape: RoundedRectangleBorder(
                  borderRadius: BorderRadius.circular(12),
                ),
              ),
              child: const Text('Done'),
            ),
            const SizedBox(height: 24),
          ],
        ),
      ),
    );
  }
}

class _SummarySection extends StatelessWidget {
  const _SummarySection({required this.title, required this.content});

  final String title;
  final String content;

  @override
  Widget build(BuildContext context) {
    return Container(
      padding: const EdgeInsets.all(16),
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(12),
        border: Border.all(color: const Color(0xFFEEF0F3)),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            title,
            style: const TextStyle(
              fontWeight: FontWeight.bold,
              color: AppTheme.textPrimary,
            ),
          ),
          const SizedBox(height: 8),
          Text(
            content,
            style: const TextStyle(color: AppTheme.textSecondary, height: 1.4),
          ),
        ],
      ),
    );
  }
}

class _SummaryListSection extends StatelessWidget {
  const _SummaryListSection({
    required this.title,
    required this.items,
    this.highlight = false,
  });

  final String title;
  final List<String> items;
  final bool highlight;

  @override
  Widget build(BuildContext context) {
    return Container(
      padding: const EdgeInsets.all(16),
      decoration: BoxDecoration(
        color: highlight
            ? AppTheme.primary.withValues(alpha: 0.03)
            : Colors.white,
        borderRadius: BorderRadius.circular(12),
        border: Border.all(
          color: highlight
              ? AppTheme.primary.withValues(alpha: 0.2)
              : const Color(0xFFEEF0F3),
        ),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            title,
            style: const TextStyle(
              fontWeight: FontWeight.bold,
              color: AppTheme.textPrimary,
            ),
          ),
          const SizedBox(height: 10),
          ...items.map(
            (item) => Padding(
              padding: const EdgeInsets.only(bottom: 8),
              child: Row(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(
                    '•  ',
                    style: TextStyle(
                      color: highlight
                          ? AppTheme.primary
                          : AppTheme.textSecondary,
                    ),
                  ),
                  Expanded(
                    child: Text(
                      item,
                      style: TextStyle(
                        color: highlight
                            ? AppTheme.textPrimary
                            : AppTheme.textSecondary,
                        height: 1.4,
                      ),
                    ),
                  ),
                ],
              ),
            ),
          ),
        ],
      ),
    );
  }
}
